/// Poses for Kai the Chameleon mascot.
///
/// Each pose maps to a specific emotional context within the app.
enum KaiPose {
  /// Arms spread wide, welcoming the user.
  /// Use: Onboarding, splash screen, first-time greeting.
  welcome,

  /// Jumping with fists pumping, celebrating success.
  /// Use: Goal achieved, milestones, positive budget status.
  celebration,

  /// Hand on chin, one eye on viewer, thinking deeply.
  /// Use: Dashboard insights, analytics, financial tips.
  thinking,

  /// Palms facing outward, cautionary stance.
  /// Use: Budget exceeded, overspending warnings.
  warning,

  /// Sitting casually on a ledge, relaxed wave.
  /// Use: Empty states, loading, general presence.
  idle,
}
